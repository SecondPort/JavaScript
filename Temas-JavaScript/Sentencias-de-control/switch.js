let numero = 2;

let numText = "valor desconocido";

switch(numero) {
    case 1:
        numText = "uno";
        break;
    case 2:
        numText = "dos";
        break;
    case 3:
        numText = "tres";
        break;
    default:
        numText = "valor desconocido";
}
console.log(numText);